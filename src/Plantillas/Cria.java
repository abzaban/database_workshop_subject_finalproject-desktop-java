package Plantillas;

public class Cria {
	
	private int Id, DiasEnferma, VecesQueEnfermo, NoProceso, DiasActiva;
	private double Peso, CantGrasa;
	private String ColorMusculo, Edo, Dieta, Clasificacion, FechaProceso, Medicamento, FechaIngreso, Estatus;
	
	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public double getPeso() {
		return Peso;
	}

	public void setPeso(double peso) {
		Peso = peso;
	}

	public double getCantGrasa() {
		return CantGrasa;
	}

	public void setCantGrasa(double cantGrasa) {
		CantGrasa = cantGrasa;
	}

	public String getColorMusculo() {
		return ColorMusculo;
	}

	public void setColorMusculo(String colorMusculo) {
		ColorMusculo = colorMusculo;
	}

	public String getEdo() {
		return Edo;
	}

	public void setEdo(String edo) {
		Edo = edo;
	}

	public String getDieta() {
		return Dieta;
	}

	public void setDieta(String dieta) {
		Dieta = dieta;
	}

	public String getClasificacion() {
		return Clasificacion;
	}

	public void setClasificacion(String clasificacion) {
		Clasificacion = clasificacion;
	}

	public String getFechaProceso() {
		return FechaProceso;
	}

	public void setFechaProceso(String fechaProceso) {
		FechaProceso = fechaProceso;
	}

	public String getEstatus() {
		return Estatus;
	}

	public void setEstatus(String estatus) {
		Estatus = estatus;
	}
	
	public String getMedicamento() {
		return Medicamento;
	}

	public void setMedicamento(String medicamento) {
		Medicamento = medicamento;
	}
	
	public int getDiasEnferma() {
		return DiasEnferma;
	}

	public void setDiasEnferma(int DiasEnferma) {
		this.DiasEnferma = DiasEnferma;
	}
	
	public int getVecesQueEnfermo() {
		return VecesQueEnfermo;
	}

	public void setVecesQueEnfermo(int vecesQueEnfermo) {
		VecesQueEnfermo = vecesQueEnfermo;
	}
	
	public int getNoProceso() {
		return NoProceso;
	}

	public void setNoProceso(int noProceso) {
		NoProceso = noProceso;
	}

	public String getFechaIngreso() {
		return FechaIngreso;
	}

	public void setFechaIngreso(String fechaIngreso) {
		FechaIngreso = fechaIngreso;
	}
	
	public int getDiasActiva() {
		return DiasActiva;
	}

	public void setDiasActiva(int diasActiva) {
		DiasActiva = diasActiva;
	}
}
